#!/usr/bin/env node

import { readFileSync } from "node:fs";

const indexHtml = readFileSync("index.html", "utf8");
const appJs = readFileSync("app.js", "utf8");
const readme = readFileSync("README.md", "utf8");

const failures = [];

requireIncludes(appJs, 'fetch("data/quran-uthmani.json"', "app must load Quran text from local reviewed data file");
requireIncludes(appJs, "els.ayahPreview.replaceChildren", "ayah preview must be rendered from selected Quran data");
requireIncludes(appJs, "els.phoneAyah.textContent", "phone preview must render Quran text programmatically");
requireIncludes(readme, "The Quran text is loaded from `data/quran-uthmani.json`. It is not generated by AI.", "README must state Quran text source and AI limit");
requireIncludes(readme, "The app must not alter Quran wording.", "README must state Quran wording is not altered");

const ayahEditablePatterns = [
  /id=["']ayahPreview["'][^>]*(contenteditable|textarea|input)/i,
  /id=["']phoneAyah["'][^>]*(contenteditable|textarea|input)/i,
  /contenteditable=["']?true["']?/i,
  /<textarea[^>]*(ayah|quran|verse)/i,
];

ayahEditablePatterns.forEach((pattern) => {
  if (pattern.test(indexHtml)) failures.push(`index.html must not expose editable Quran text: ${pattern}`);
});

[
  "els.ayahPreview.value",
  "els.phoneAyah.value",
  "setAttribute(\"contenteditable\"",
  "setAttribute('contenteditable'",
].forEach((token) => {
  if (appJs.includes(token)) failures.push(`app.js must not make Quran text editable: ${token}`);
});

if (failures.length) {
  console.error(failures.join("\n"));
  process.exit(1);
}

console.log("PASS Quran text policy prevents manual editing and documents the source.");

function requireIncludes(content, token, message) {
  if (!content.includes(token)) failures.push(message);
}
